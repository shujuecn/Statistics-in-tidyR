library(tibble)
library(dplyr)

# 逐列输入数据
df1 = tibble(
  `编号` = 1:3,
  `姓名` = c("李晓华", "张亮亮", "蔡静"),
  `性别` = c(2, 1, 2),
  `出生日期` = c("06/30/2003", "12/15/2002", "04/21/2004"),
  `体重` = c(2.80, 4.15, 3.00),
  `身长` = c(40.00, 53.62, 46.21)
)
df1

# 逐行输入数据
df2 = tribble(
  ~编号, ~姓名, ~性别, ~出生日期, ~体重, ~身长,
  1, "李晓华", 2, "06/30/2003", 2.80, 40.00,
  2, "张亮亮", 1, "12/15/2002", 4.15, 53.62,
  3, "蔡静", 2, "04/21/2004", 3.00, 46.21
)
df2
